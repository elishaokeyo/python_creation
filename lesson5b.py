# function with parameters 
def add(num1,num2):
    sum=num1+num2
    print(sum)
add(20, 30)    
add(100,200)
add(500,600)


def sub(num1, num2):
    sub=num1-num2
    print(sub)
sub(20,30)    

def div(num1,num2):
    div=num1/num2
    print(div)
div(30,20)


def mult(num1,num2):
    mult=num1*num2
    print(mult)
mult(30,20)    


def area(pi,r):
    area=pi*r**2
    print(area)
area(22/7,7)   

def bmi(weight,height):
    bmi=weight/height**2
    print(bmi)
bmi(40000,20)    
